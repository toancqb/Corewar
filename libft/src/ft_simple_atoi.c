int	ft_simple_atoi(const char **str)
{
	int	res;

	res = 0;
	while ((*str)[0] >= '0' && (*str)[0] <= '9')
	{
		res *= 10;
		res += ((*str)[0] - '0');
		(*str)++;
	}
	return (res);
}
