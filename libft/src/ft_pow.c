#include "libft.h"

int	ft_pow(int n, int pow)
{
	int	nb;

	if (pow == 0)
		return (1);
	if (pow == 1)
		return (n);
	nb = n;
	while (--pow > 0)
		nb *= n;
	return (nb);
}
